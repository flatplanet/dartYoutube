void main() {
	// Data Types
	// Numbers, Strings, Booleans, Lists, Maps
	// Integers (int) Double (double) 
	// Strings
	// Booleans = bool
	// dynamic, if the type is not specified, dart implies dynamic



	String name = "John";
	print("String: $name");

	int myNum = 41;
	print("Int: $myNum");

	double otherNum = 41.95;
	print("Double: $otherNum");

	bool myBool = false;
	print("Boolean: $myBool");

	dynamic fullName = "John Elder";
	print("Dynamic: $fullName");

}